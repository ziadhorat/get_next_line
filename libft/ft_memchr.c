#include "libft.h"

void	*ft_memchr(void const *s, int c, size_t n)
{
	unsigned int		i;

	i = 0;
	while (i < n)
	{
		if (((unsigned const char *)s)[i] == (unsigned char)c)
			return ((void *)&((unsigned const char *)s)[i]);
		i += 1;
	}
	return (NULL);
}
